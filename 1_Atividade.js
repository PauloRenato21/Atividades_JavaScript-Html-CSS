
function soma(valor1,valor2){
    valor = valor1 + valor2;
    return valor
}

function subtrair(valor1, valor2){
    valor = valor1 - valor2
    return valor
}

function multi(valor1, valor2){
    valor = valor1 * valor2
    return valor
}
function divi(valor1, valor2){
    valor = valor1 / valor2
    return valor
}

console.log(soma (10,10))
console.log("-----------------------")
console.log(subtrair(12,23))
console.log("-----------------------")
console.log(multi(34,5))
console.log("-----------------------")
console.log(divi(100,3))
